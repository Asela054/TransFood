    public function Factoryedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_factory');
        $this->db->where('idtbl_factory', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_factory;
        $obj->name=$respond->row(0)->factoryname;
        $obj->code=$respond->row(0)->factorycode;
        $obj->contact=$respond->row(0)->contact;
        $obj->contact2=$respond->row(0)->contact2;
        $obj->email=$respond->row(0)->email;
        $obj->address=$respond->row(0)->address;

        echo json_encode($obj);
    }
