    public function Invoicebankedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_invoice_bank');
        $this->db->where('idtbl_invoice_bank', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_invoice_bank;
        $obj->account_name=$respond->row(0)->account_name;
        $obj->account_no=$respond->row(0)->account_no;
        $obj->bank_name=$respond->row(0)->bank_name;
        $obj->bank_branch=$respond->row(0)->bank_branch;
        $obj->swift_code=$respond->row(0)->swift_code;
        $obj->branch_code=$respond->row(0)->branch_code;
        $obj->bank_code=$respond->row(0)->bank_code;

        echo json_encode($obj);
    }
