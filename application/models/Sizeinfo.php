    public function Sizeedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_size');
        $this->db->where('idtbl_size', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_size;
        $obj->sizename=$respond->row(0)->sizename;
        $obj->sizecode=$respond->row(0)->sizecode;
        $obj->materialcategory=$respond->row(0)->tbl_material_category_idtbl_material_category ;

        echo json_encode($obj);
    }
