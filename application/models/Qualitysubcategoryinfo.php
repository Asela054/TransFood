    public function Qualitysubcategoryedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_quality_subcategory');
        $this->db->where('idtbl_quality_subcategory', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_quality_subcategory;
        $obj->qualitysubcategory=$respond->row(0)->qualitysubcategory;
        $obj->inputtype=$respond->row(0)->inputtype;
        $obj->category=$respond->row(0)->tbl_quality_category_idtbl_quality_category;

        echo json_encode($obj);
    }
